import java.util.*;
public class printn{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int n;
        System.out.println("Enter the value of n");
        n=sc.nextInt();
        int i=1;
        while(i<=n){
            System.out.println(i);
            i++;
        }

    }
}